    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        
       
        for(int i=0 ; i < release.size()-1 ; i++){
            PParray.add((long) press.get(i+1) - (long)press.get(i));
            RRarray.add((long) release.get(i+1) - (long)release.get(i));
            PRarray.add((long)release.get(i) - (long)press.get(i));
        }
        PRarray.add((long)release.get(press.size()-1) - (long)press.get(press.size()-1));
        for(int j=1 ; j < release.size() ; j++){
            RParray.add((long) press.get(j) - (long)release.get(j-1));
        }  
        user user = new user(keys,PParray,PRarray,RParray,RRarray);
        user.login(this);
        
    }//GEN-LAST:event_jButton1ActionPerformed




