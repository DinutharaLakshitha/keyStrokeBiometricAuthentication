    private void jEditorPane1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jEditorPane1KeyPressed
        char s = evt.getKeyChar();
        keys.add(s);
        long timep = System.currentTimeMillis();
        press.add(timep);
        //System.out.print(Long.toString(timep));
        if (evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {
        jEditorPane1.setText("");
        press.clear();
        release.clear();
        keys.clear();
    }
        
        
       
    }//GEN-LAST:event_jEditorPane1KeyPressed

 
    
    
    
    
